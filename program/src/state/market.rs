impl Versioned for Market {
    // v10 (sharding): appended `num_slab_shards` + the `shards_pending` (completeness)
    // and `shards_ready` (roll) aggregates. A market now has `num_slab_shards` slabs;
    // the single-slab layout is gone, so a pre-v10 market must be re-provisioned (the
    // version bump fails it loudly). An append is prefix-compatible for readers, but the
    // account is sized by `DATA_LEN`, so a shorter pre-v10 account fails the size check.
    //
    // v9 (PERF-1): removed the redundant `accumulated_order_count` /
    // `active_order_count` mirror fields so `submit_order`/`cancel_order` no longer
    // write-lock `Market` (known-issues §2.1). The authoritative live-order count is
    // `OrderSlabHeader.count` and the authoritative folded count is
    // `AuctionHistogram.accumulated_count`; the completeness/censorship guarantee in
    // `finalize_clear` already rests on the `all_active_orders_accumulated` slab scan,
    // not on these counters. Removing fields from the prefix is NOT append-compatible,
    // so a pre-v9 account must be re-provisioned (the version bump fails it loudly).
    //
    // v8: appended `initial_margin_bps` (the initial-margin buffer above maintenance)
    // and `max_position_notional` (per-position notional cap) for pre-trade safety
    // (missing-features §1.1/§1.2). An append is layout-compatible for prefix readers,
    // but the account is sized by `DATA_LEN`, so a pre-v8 account is shorter and must
    // be re-provisioned; the version bump fails a stale account loudly.
    //
    // v7: appended `window_floor_price` for the oracle-anchored tick window
    // (known-issues §2.7). An append IS layout-compatible for readers of the
    // prefix, but the account is sized by `DATA_LEN`, so a pre-v7 account is
    // shorter and must be re-provisioned (dev-phase: re-provision is free). The
    // version bump makes a stale account fail the zero-copy check loudly.
    //
    // v6: the dead `sync_fee_multiplier` byte was removed from the prefix
    // (known-issues §3). A prefix change is NOT append-compatible, so that bump
    // made any pre-v6 account fail the zero-copy version check loudly instead of
    // decoding off-by-one — old accounts must be re-provisioned, not migrated.
    const VERSION: u8 = 10;
}

