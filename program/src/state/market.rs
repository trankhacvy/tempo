/// Slots the `Collect` window stays open before a crank may start accumulating
/// (system-design §3, freeze model). `process_chunk` refuses to fold the first
/// chunk until `phase_deadline_slot` is reached, so orders submitted anywhere in
/// the window land in the same batch. Production tunes this per market; this uses a
/// protocol constant (off-chain cranks can extend it via their own pacing).
pub const COLLECT_WINDOW_SLOTS: u64 = 2;

/// Auction phase for a market's current round.
///
/// Drives the three-phase clearing protocol (clearing-protocol §3):
/// `Collect` accepts/cancels orders → `Accumulating` folds them into the
/// histogram → `Discovered` has a published `ClearingResult` → `Settling`
/// lets users pull their fills.
#[repr(u8)]
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum AuctionPhase {
    Collect = 0,
    Accumulating = 1,
    Discovered = 2,
    Settling = 3,
}

impl AuctionPhase {
    #[inline(always)]
    pub fn from_u8(value: u8) -> Result<Self, ProgramError> {
        match value {
            0 => Ok(Self::Collect),
            1 => Ok(Self::Accumulating),
            2 => Ok(Self::Discovered),
            3 => Ok(Self::Settling),
            _ => Err(TempoProgramError::AuctionWrongPhase.into()),
        }
    }
}

/// Market account state (the per-market control block).
///
/// Carries the clearing control block plus the funding index and
/// per-market risk config (maintenance/penalty/fee bps, crank fee). The `oracle`
/// is consumed by `update_funding`/`liquidate`/`read_oracle`.
///
/// # PDA Seeds
/// `[b"market", market_seed.as_ref()]`
///
/// # Zero-copy layout (`#[repr(C)]`, **alignment 1**)
/// All multi-byte integers are stored as little-endian byte arrays so the
/// struct has alignment 1. Account data is pointer-cast at byte offset 2 (after
/// the disc+version prefix), which is not 8-byte aligned, so native `u64`
/// fields would be an unaligned read. Accessors are generated by `le_field!`.
///
/// 5 × [u8;8] (40) + 2 × [u8;4] (8) + 3 × Address (96) + 2 × u8 (2) = 146.
#[derive(Clone, Debug, PartialEq, CodamaAccount)]
#[codama(field("discriminator", number(u8), default_value = 1))]
#[codama(discriminator(field = "discriminator"))]
#[codama(seed(type = string(utf8), value = "market"))]
#[codama(seed(name = "marketSeed", type = public_key))]
#[repr(C)]
pub struct Market {
    pub current_auction_id_le: [u8; 8],
    pub phase_deadline_slot_le: [u8; 8],
    pub tick_size_le: [u8; 8],
    pub last_bid_fill_price_le: [u8; 8],
    pub last_ask_fill_price_le: [u8; 8],
    pub orders_per_auction_cap_le: [u8; 4],
    pub num_ticks_le: [u8; 4],
    pub authority: Address,
    pub market_seed: Address,
    pub oracle: Address,
    /// Current auction phase (`AuctionPhase`).
    pub phase: u8,
    /// PDA bump.
    pub bump: u8,
    // --- funding (appended; keeps earlier field offsets stable) ---
    /// Monotonic funding index (signed, `funding.rs` `FUNDING_SCALE` units).
    pub funding_index_le: [u8; 16],
    /// Unix timestamp of the last `update_funding` (for rate-limiting accrual).
    pub last_funding_ts_le: [u8; 8],
    // --- per-market risk config (appended; offsets above stay stable) ---
    /// Pyth feed id this market's `oracle` account must carry (`oracle.rs`).
    pub oracle_feed_id: [u8; 32],
    /// Maintenance margin requirement in bps (per-market risk param).
    pub maintenance_margin_bps_le: [u8; 2],
    /// Liquidation penalty in bps (per-market risk param).
    pub liquidation_penalty_bps_le: [u8; 2],
    /// Maker fee in bps, SIGNED (negative = maker rebate); applied when `is_maker`.
    pub maker_fee_bps_le: [u8; 2],
    /// Taker fee in bps, SIGNED (negative = taker rebate); applied to takers.
    pub taker_fee_bps_le: [u8; 2],
    /// Integrator revenue share, in bps of the positive fee paid (0..=10_000).
    pub integrator_share_bps_le: [u8; 2],
    /// Flat fee paid to the `finalize_clear` cranker from the fee pool.
    pub crank_fee_le: [u8; 8],
    // --- Vault binding (appended; offsets above stay stable) ---
    /// Collateral mint this market settles in; binds the per-mint vault
    /// `[b"vault", collateral_mint]` to the market so a foreign vault cannot be
    /// supplied. Zero (`[0u8; 32]`) means no declared money path.
    pub collateral_mint: Address,
    // --- Maker quotes (VERSION 4; per-maker parametric liquidity) ---
    /// Next id assigned to a new `MakerQuote` (the marginal-tick tie-break order).
    pub next_quote_id_le: [u8; 8],
    /// Number of active maker quotes on this market (completeness denominator).
    pub active_maker_quote_count_le: [u8; 8],
    /// Maker quotes folded into the histogram this round (reset each round).
    pub folded_maker_quote_count_le: [u8; 8],
    // --- risk hardening (VERSION 5; appended, offsets stable) ---
    /// Total open long base units across all positions (open-interest tracking).
    pub oi_long_le: [u8; 16],
    /// Total open short base units across all positions (open-interest tracking).
    pub oi_short_le: [u8; 16],
    /// Socialized-loss index charged to the long side (signed, `FUNDING_SCALE` units, ADL).
    pub social_loss_index_long_le: [u8; 16],
    /// Socialized-loss index charged to the short side (signed, `FUNDING_SCALE` units, ADL).
    pub social_loss_index_short_le: [u8; 16],
    /// Last accepted effective mark (1e8) used for risk; advances ≤ cap per slot.
    pub effective_price_1e8_le: [u8; 8],
    /// Slot the effective price was last advanced.
    pub effective_price_slot_le: [u8; 8],
    /// Slot of the most recent fresh oracle update (staleness tracking).
    pub last_good_oracle_slot_le: [u8; 8],
    /// Max per-slot effective-price move in bps (meltdown brake; risk param).
    pub max_price_move_bps_per_slot_le: [u8; 2],
    /// Slots past `last_good_oracle_slot` before the feed is treated hard-stale (risk param).
    pub soft_stale_slots_le: [u8; 8],
    // --- oracle-anchored tick window (VERSION 7; appended, offsets stable) ---
    /// Price represented by histogram tick 0 this round (known-issues §2.7). The
    /// window is `[floor, floor + (num_ticks-1)·tick_size]`; `price_to_tick` maps
    /// `price → (price − floor)/tick_size`. Re-snapped onto the oracle at every
    /// `start_auction` (and `initialize_market`), and **frozen for the whole round**
    /// — the histogram is indexed by tick, so the anchor must not move mid-round.
    /// A stale/missing oracle carries the previous floor forward (never blocks the
    /// roll). Genesis default is `tick_size` (one tick), which reproduces the old
    /// zero-anchored window exactly.
    pub window_floor_price_le: [u8; 8],
    // --- pre-trade risk config (VERSION 8; appended, offsets stable) ---
    /// Initial-margin requirement in bps — the buffer locked at open/increase,
    /// **above** `maintenance_margin_bps`, so a position never opens exactly on its
    /// liquidation line (missing-features §1.2). Its inverse is the market's implicit
    /// max leverage (e.g. 1000 bps ⇒ 10×). Validated `≥ maintenance_margin_bps` at
    /// init; `0` ⇔ no money path (paired with `maintenance_margin_bps == 0`).
    pub initial_margin_bps_le: [u8; 2],
    /// Maximum notional (`|size|·entry`) a single position may hold, enforced at
    /// `submit_order` against the order's worst-case resulting position
    /// (missing-features §1.2). `0` = disabled. A per-position risk bound that the
    /// leverage limit does not give (a whale at low leverage).
    pub max_position_notional_le: [u8; 16],
    // --- sharding (VERSION 10; appended, offsets stable) ---
    /// Number of `OrderSlab` shards for this market (set at init). Orders are routed
    /// across shards `[0, num_slab_shards)` so submit/settle run in parallel.
    pub num_slab_shards_le: [u8; 2],
    /// Shards not yet fully folded THIS round — the O(1) completeness aggregate
    /// `finalize_clear` gates on. Each shard decrements it once when its
    /// `resting_count` reaches 0 (confirmed by a per-shard scan). Reset to
    /// `num_slab_shards` at each roll.
    pub shards_pending_le: [u8; 2],
    /// Shards reset (drained + zeroed) for the next round — the roll aggregate
    /// `start_auction` gates on. Each `reset_shard` increments it once; reset to 0 at
    /// roll (see clearing-protocol §4, freeze model).
    pub shards_ready_le: [u8; 2],
}

