    /// Apply a signed open-interest delta from a position size change
    /// (`old_signed` → `new_signed`, base units). Saturating; keeps `oi_long` and
    /// `oi_short` as the running totals of long and short exposure.
    pub fn apply_oi_delta(&mut self, old_signed: i128, new_signed: i128) {
        let old_long = old_signed.max(0) as u128;
        let new_long = new_signed.max(0) as u128;
        let old_short = (-old_signed).max(0) as u128;
        let new_short = (-new_signed).max(0) as u128;
        self.set_oi_long(
            self.oi_long()
                .saturating_add(new_long)
                .saturating_sub(old_long),
        );
        self.set_oi_short(
            self.oi_short()
                .saturating_add(new_short)
                .saturating_sub(old_short),
        );
    }

    /// Socialize an uncovered bad-debt `residual` to the winning (counterparty)
    /// side by its open interest (ADL): a liquidated long's residual is
    /// charged to shorts, and vice-versa, via `+= residual·FUNDING_SCALE/winner_oi`.
    /// Returns `false` (without mutating) when there is no winning OI to absorb it,
    /// so the caller can log the unbacked remainder.
    pub fn socialize_bad_debt(
        &mut self,
        loser_signed_size: i128,
        residual: u64,
    ) -> Result<bool, ProgramError> {
        if residual == 0 {
            return Ok(true);
        }
        // A liquidated long (size > 0) is funded by the shorts → charge shorts.
        let winner_is_long = loser_signed_size < 0;
        let winner_oi = if winner_is_long {
            self.oi_long()
        } else {
            self.oi_short()
        };
        if winner_oi == 0 {
            return Ok(false);
        }
        // Round the per-unit index increment UP (against winners): the per-position
        // charge in `Position::settle_social_loss` floors again, so a floored index
        // here double-floors and makes `Σ charges < residual` — leaving dust the last
        // winner's settle can't cover, reverting it `InsuranceInsolvent`. Ceiling the
        // index increment makes `winner_oi · add / FUNDING_SCALE ≥ residual`, so the
        // socialized charges cover the residual instead of falling short (HS-8).
        let add = crate::wide_math::mul_div_ceil(
            residual as u128,
            crate::funding::FUNDING_SCALE as u128,
            winner_oi,
        )
        .ok_or(TempoProgramError::MathOverflow)?;
        let add = i128::try_from(add).map_err(|_| TempoProgramError::MathOverflow)?;
        if winner_is_long {
            self.set_social_loss_index_long(self.social_loss_index_long().saturating_add(add));
        } else {
            self.set_social_loss_index_short(self.social_loss_index_short().saturating_add(add));
        }
        Ok(true)
    }

    /// Walk the stored effective risk price toward a fresh `oracle_1e8` by at most
    /// the per-slot cap (brake), persist it + the freshness stamp, and return
    /// the new mark.
    pub fn advance_effective_price(&mut self, oracle_1e8: u64, now_slot: u64) -> u64 {
        let new_eff = crate::mark::clamp_price_step(
            self.effective_price_1e8(),
            oracle_1e8,
            self.max_price_move_bps_per_slot(),
            now_slot.saturating_sub(self.effective_price_slot()),
        );
        self.set_effective_price_1e8(new_eff);
        self.set_effective_price_slot(now_slot);
        self.set_last_good_oracle_slot(now_slot);
        new_eff
    }

    /// Re-snap the tick window onto a fresh `oracle_1e8`, centering it so the
    /// oracle sits in the middle of the `num_ticks` band (known-issues §2.7):
    /// `floor = oracle − (num_ticks/2)·tick_size`, snapped down to a tick multiple
    /// and clamped to at least one tick (price 0 is invalid). Called only when a
    /// round opens (`start_auction`/`initialize_market`), never mid-round, so the
    /// histogram's tick→price meaning is constant for the whole round. A zero
    /// `oracle_1e8` (no usable price) is a no-op — the caller carries the previous
    /// floor forward.
    pub fn recenter_window(&mut self, oracle_1e8: u64) {
        let tick_size = self.tick_size();
        if oracle_1e8 == 0 || tick_size == 0 {
            return;
        }
        let half = (self.num_ticks() as u64 / 2).saturating_mul(tick_size);
        let raw_floor = oracle_1e8.saturating_sub(half);
        // Snap down to a tick multiple so every in-window price stays a clean
        // multiple of `tick_size` (keeps `validate_price` and `price_to_tick`
        // consistent), and never below one tick.
        let snapped = (raw_floor / tick_size).saturating_mul(tick_size);
        self.set_window_floor_price(snapped.max(tick_size));
    }

